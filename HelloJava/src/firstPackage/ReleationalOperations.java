package firstPackage;

public class ReleationalOperations {

	public static void main(String[] args) {
		 int x = 10 ;
		 int y = 5 ;
		 
		 //equal to 
		 System.out.println("Is x equal Y ?" + (x==y));
		 //Not equal to
		 System.out.println("Is x not equal Y ?" + (x!=y));
		 //Greater Than
		 System.out.println("Is x Greater than Y ?" + (x > y));
		 //Greater than or equal
		 System.out.println("Is x Greater than or equal Y ?" + (x>=y));
		 //less than
		 System.out.println("Is x less than Y ?" + (x<y));
		 //less than or equal 
		 System.out.println("Is x less than or equal  Y ?" + (x<=y));

	}

}
