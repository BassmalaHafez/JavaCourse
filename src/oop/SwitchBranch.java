package oop;

public class SwitchBranch {
    public static void main (String[] args){
        int day = 6 ;
        switch (day)
        {
            case 1:
                System.out.println("Sunday is the first day of the week ");
                break;
            case 2:
                System.out.println("Monday is the first day of the week ");
                break;
            case 3:
                System.out.println("Tuesday is the first day of the week ");
                break;
            case 4:
                System.out.println("Wednesday is the first day of the week ");
                break;
            case 5:
                System.out.println("Thursday is the first day of the week ");
                break;
            case 6:
                System.out.println("Friday is the first day of the week ");
                break;
            case 7:
                System.out.println("Thursday  is the first day of the week ");
                break;
            default:
                System.out.println("Not Valid ");
        }
    }

}
